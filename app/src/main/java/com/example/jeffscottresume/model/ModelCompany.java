package com.example.jeffscottresume.model;

public class ModelCompany {
    public String companyName;
    public String companyCity;
    public String companyState;
    public String companyPeriod;
    public String companyLogo;

    public String getCompanyName() {
        return companyName;
    }

    public void setCompanyName(String companyName) {
        this.companyName = companyName;
    }

    public String getCompanyCity() {
        return companyCity;
    }

    public void setCompanyCity(String companyCity) {
        this.companyCity = companyCity;
    }

    public String getCompanyState() {
        return companyState;
    }

    public void setCompanyState(String companyState) {
        this.companyState = companyState;
    }

    public String getCompanyPeriod() {
        return companyPeriod;
    }

    public void setCompanyPeriod(String companyPeriod) {
        this.companyPeriod = companyPeriod;
    }

    public String getCompanyLogo() {
        return companyLogo;
    }

    public void setCompanyLogo(String companyLogo) {
        this.companyLogo = companyLogo;
    }
}
