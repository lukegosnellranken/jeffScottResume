package com.example.jeffscottresume.model;

public class ModelSchool {
    public String schoolName;
    public String schoolDegreeName;
    public String schoolGraduationDate;
    public String schoolImageLink;

    public String getSchoolName() {
        return schoolName;
    }

    public void setSchoolName(String schoolName) {
        this.schoolName = schoolName;
    }

    public String getSchoolDegreeName() {
        return schoolDegreeName;
    }

    public void setSchoolDegreeName(String schoolDegreeName) {
        this.schoolDegreeName = schoolDegreeName;
    }

    public String getSchoolGraduationDate() {
        return schoolGraduationDate;
    }

    public void setSchoolGraduationDate(String schoolGraduationDate) {
        this.schoolGraduationDate = schoolGraduationDate;
    }

    public String getSchoolImageLink() {
        return schoolImageLink;
    }

    public void setSchoolImageLink(String schoolImageLink) {
        this.schoolImageLink = schoolImageLink;
    }
}
